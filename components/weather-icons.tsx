/**
 * Retorna o componente de icone adequado com base no codigo WMO.
 */
export function getWeatherIcon(weatherCode: number) {
  if ([0, 1].includes(weatherCode)) return SunIcon
  if ([2, 3, 45, 48].includes(weatherCode)) return CloudIcon
  if ([51, 53, 55, 56, 57, 61, 63, 65, 66, 67, 80, 81, 82].includes(weatherCode)) return RainIcon
  if ([71, 73, 75, 77, 85, 86].includes(weatherCode)) return SnowIcon
  if ([95, 96, 99].includes(weatherCode)) return StormIcon
  return CloudIcon
}

/**
 * Retorna a categoria do clima para estilizacao dinamica do fundo.
 */
export function getWeatherCategory(weatherCode: number): 'clear' | 'cloudy' | 'rain' | 'snow' | 'storm' {
  if ([0, 1].includes(weatherCode)) return 'clear'
  if ([2, 3, 45, 48].includes(weatherCode)) return 'cloudy'
  if ([51, 53, 55, 56, 57, 61, 63, 65, 66, 67, 80, 81, 82].includes(weatherCode)) return 'rain'
  if ([71, 73, 75, 77, 85, 86].includes(weatherCode)) return 'snow'
  if ([95, 96, 99].includes(weatherCode)) return 'storm'
  return 'cloudy'
}
